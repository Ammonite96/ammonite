package TP1JavaRush4lvl;

public class TableUmn {
    public static void main(String[] args) {
        int i = 0, j = 0, a = 0, b = 0, c = 0, d = 0, f = 0, q = 0, w = 0, e = 0;
        while (i < 10) {
            i++;
            System.out.print(" " + i);
            while (j < 2)
                j++;
            System.out.print(" " + i * j);
            while (a < 3)
                a++;
            System.out.print(" " + i * a);
            while (b < 4)
                b++;
            System.out.print(" " + i * b);
            while (c < 5)
                c++;
            System.out.print(" " + i * c);
            while (d < 6)
                d++;
            System.out.print(" " + i * d);
            while (f < 7)
                f++;
            System.out.print(" " + i * f);
            while (q < 8)
                q++;
            System.out.print(" " + i * q);
            while (w < 9)
                w++;
            System.out.print(" " + i * w);
            while (e < 10)
                e++;
            System.out.print(" " + i * e);
            System.out.println();
        }
    }
}

