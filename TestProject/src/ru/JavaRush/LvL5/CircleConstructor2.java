package ru.JavaRush.LvL5;

/**
 * В классе Circle создать конструктор который проинициализирует все переменные класса.
 * В конструкторе должно быть три аргумента.
 */

public class CircleConstructor2 {

    public double x;
    public double y;
    public double r;

    public CircleConstructor2(double x, double y, double r) {
        this.x = x;
        this.y = y;
        this.r = r;
    }
}
